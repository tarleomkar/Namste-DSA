console.log(Math.abs(-5));    // Output: 5
console.log(Math.abs(10));    // Output: 10
console.log(Math.abs(0));     // Output: 0
console.log(Math.abs(-0));    // Output: 0
console.log(Math.abs("7"));   // Output: 7 (numeric string treated as a number)
console.log(Math.abs("-3.14")); // Output: 3.14 (numeric string treated as a number)
console.log(Math.abs("hello")); // Output: NaN (non-numeric string)
console.log(Math.abs(null));  // Output: 0